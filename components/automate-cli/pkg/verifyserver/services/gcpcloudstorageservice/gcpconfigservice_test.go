type MockGCPUtils struct {
	TestProbe               *testing.T
	InvalidCredentials      bool
	IsBucketAttributesError bool
	IsUploadError           bool
	IsListError             bool
	IsDeleteObjError        bool
}

func (au *MockGCPUtils) NewSessionWithOptions(ctx context.Context, gsa *models.GcpServiceAccount) (*storage.Client, error) {
	assert.Equal(au.TestProbe, "65464654684864353165486", gsa.ClientID)
	if au.InvalidCredentials {
		return nil, errors.New("Invalid Credentials")
	}
	bx, _ := json.Marshal(gsa)
	return storage.NewClient(ctx, option.WithCredentialsJSON(bx))
}

func (au *MockGCPUtils) NewUploader(ctx context.Context, uploadObject *storage.ObjectHandle) error {
	assert.Equal(au.TestProbe, "backups", uploadObject.BucketName())
	assert.Contains(au.TestProbe, uploadObject.ObjectName(), "gcp_check_test")
	if au.IsUploadError {
		return errors.New("error in uploading the object")
	}
	return nil
}

func (au *MockGCPUtils) DeleteObject(ctx context.Context, obj *storage.ObjectHandle) error {
	if au.IsDeleteObjError {
		return errors.New("error in deleting the object from the bucket")
	}
	return nil
}

func (au *MockGCPUtils) ListObjects(ctx context.Context, bucket *storage.BucketHandle, query *storage.Query) error {
	if au.IsListError {
		return errors.New("error in listing the object")
	}
	return nil
}

func (au *MockGCPUtils) BucketAttributes(ctx context.Context, bucket *storage.BucketHandle) error {
	if au.IsBucketAttributesError {
		return errors.New("error in getting bucket attributes")
	}
	return nil
}

